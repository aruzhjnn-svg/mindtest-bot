# ----- –û—Ç–ø—Ä–∞–≤–∫–∞ –≤–æ–ø—Ä–æ—Å–∞ -----
async def send_question(update_obj, context, user_id):
    # update_obj –º–æ–∂–µ—Ç –±—ã—Ç—å update.callback_query –∏–ª–∏ update.message
    data = user_data[user_id]
    q_index = data["q"]

    # –ï—Å–ª–∏ –≤–æ–ø—Ä–æ—Å–æ–≤ –±–æ–ª—å—à–µ –Ω–µ—Ç ‚Äî –ø–æ–∫–∞–∑–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç
    if q_index >= len(QUESTIONS):
        return await show_result(update_obj, context, user_id)

    question, options = QUESTIONS[q_index]
    # –ö–Ω–æ–ø–∫–∏: —Ç—Ä–∏ –≤–∞—Ä–∏–∞–Ω—Ç–∞
    kb = [
        [InlineKeyboardButton("‚úÖ –°–æ–≥–ª–∞—Å–Ω–∞", callback_data=f"ans|{q_index}|0")],
        [InlineKeyboardButton("‚ûñ –ù–µ —É–≤–µ—Ä–µ–Ω–∞", callback_data=f"ans|{q_index}|1")],
        [InlineKeyboardButton("‚ùå –ù–µ —Å–æ–≥–ª–∞—Å–Ω–∞", callback_data=f"ans|{q_index}|2")],
    ]
    progress = f"–í–æ–ø—Ä–æ—Å {q_index+1} –∏–∑ {len(QUESTIONS)}"
    text = f"üîé {question}\n\n{progress}"
    if isinstance(update_obj, type(context.application.bot)):
        # unlikely
        pass

    if hasattr(update_obj, "callback_query") and update_obj.callback_query:
        await update_obj.callback_query.edit_message_text(text, reply_markup=InlineKeyboardMarkup(kb))
    else:
        await update_obj.message.reply_text(text, reply_markup=InlineKeyboardMarkup(kb))

# ----- –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–Ω–æ–ø–æ–∫ –æ—Ç–≤–µ—Ç–æ–≤ -----
async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data

    user_id = query.from_user.id
    if user_id not in user_data:
        # –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º, –µ—Å–ª–∏ –≤–¥—Ä—É–≥
        user_data[user_id] = {"q": 0, "scores": {k: 0 for k in TYPES.keys()}, "answers": []}

    # –ù–∞–≤–∏–≥–∞—Ü–∏—è: —Å—Ç–∞—Ä—Ç —Ç–µ—Å—Ç–∞ –∏–ª–∏ –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ
    if data == "start_test":
        await send_question(update, context, user_id)
        return
    if data == "explain":
        await explain_intro(update, context)
        return
    if data == "retake":
        # —Å–±—Ä–æ—Å –∏ —Å—Ç–∞—Ä—Ç –∑–∞–Ω–æ–≤–æ
        user_data[user_id] = {"q": 0, "scores": {k: 0 for k in TYPES.keys()}, "answers": []}
        await send_question(update, context, user_id)
        return

    # –û—Ç–≤–µ—Ç—ã —Ñ–æ—Ä–º–∞—Ç–∞ ans|{q_index}|{option_index}
    if data.startswith("ans|"):
        try:
            _, q_str, opt_str = data.split("|")
            q_index = int(q_str)
            opt_index = int(opt_str)
        except Exception:
            await query.edit_message_text("–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –≤ –æ–±—Ä–∞–±–æ—Ç–∫–µ –æ—Ç–≤–µ—Ç–∞. –ü–æ–ø—Ä–æ–±—É–π –µ—â—ë —Ä–∞–∑.")
            return

        # –ï—Å–ª–∏ q_index –Ω–µ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å –æ–∂–∏–¥–∞–µ–º—ã–º ‚Äî –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º –∏–ª–∏ —Å–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∏—Ä—É–µ–º
        user_state = user_data[user_id]
        if q_index != user_state["q"]:
            # —Å–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∏—Ä—É–µ–º: —É—Å—Ç–∞–Ω–æ–≤–∏–º q = q_index (–±–µ–∑ —à—Ç—Ä–∞—Ñ–æ–≤)
            user_state["q"] = q_index

        # –ó–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –æ—Ç–≤–µ—Ç: –¥–æ–±–∞–≤–∏–º –≥–æ–ª–æ—Å–∞ –¥–ª—è —Ç–∏–ø–æ–≤, —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏—Ö –≤–∞—Ä–∏–∞–Ω—Ç—É
        _, options = QUESTIONS[q_index]
        selected_types = options[opt_index]  # —Å–ø–∏—Å–æ–∫ —Ç–∏–ø–æ–≤
        for t in selected_types:
            if t in user_state["scores"]:
                user_state["scores"][t] += 1

        user_state["answers"].append((q_index, opt_index, selected_types))
        user_state["q"] += 1

        # –æ—Ç–ø—Ä–∞–≤–∏–º —Å–ª–µ–¥—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å –∏–ª–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
        if user_state["q"] < len(QUESTIONS):
            await send_question(update, context, user_id)
        else:
            await show_result(update, context, user_id)
        return

    # –î–æ–ø. –∫–Ω–æ–ø–∫–∏: –ø–æ–¥—Ä–æ–±–Ω–µ–µ –ø–æ —Ç–∏–ø—É / –ø–æ–¥–µ–ª–∏—Ç—å—Å—è
    if data.startswith("more|"):
        _, type_key = data.split("|")
        if type_key in TYPES:
            info = TYPES[type_key]
            text = f"‚ú® *{info['title']}*\n\n{info['desc']}\n\nüí° *–°–æ–≤–µ—Ç*: {info['advice']}\n\n–ü—Ä–æ–¥–æ–ª–∂–µ–Ω–∏–µ —Å–ª–µ–¥—É–µ—Ç..."
            await query.edit_message_text(text, parse_mode="Markdown")
        else:
            await query.edit_message_text("–ò–Ω—Ñ–æ –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–æ.")
        return

